'use strict';

let integerNumber = 6;
console.log ("Integer: " + typeof integerNumber);

let floatingPointNumber = 6.66;
console.log ("Floating point: " + typeof floatingPointNumber);

let array = [ 1, 2, 3];
console.log ("Array: " + typeof array);

let booleanTrue = true;
console.log ("Boolean true: " + typeof booleanTrue);

let booleanFalse = false;
console.log("Boolean false: " + typeof booleanFalse);

let object = { a: 1, b: 2 };
console.log("Simple object: " + typeof object);

let string = 'Hello World';
console.log ("String: " + typeof string);

let undef;
console.log("Undefined: " + typeof undef);

let nil = null;
console.log ("Null: " + typeof nil);